    <p>This Copyright Notice explains how copyright owners can request removal of content that infringes their
        intellectual property rights, and how users can respond to such requests.</p>

    <h2 id="our-role">2. Our Role as a Platform</h2>
    <p>The Forge serves as a platform that hosts user-generated content, including modifications, tools, and related
        materials for Single Player Tarkov. We do not review all content for copyright compliance before it is posted.
    </p>

    <p><strong>Important Note:</strong> The Forge does not make legal determinations about whether content infringes
        copyright. We act as an intermediary, processing valid DMCA notices and counter-notices according to the
        procedures outlined in this policy.</p>

    <h2 id="response-procedure">3. Response Procedure</h2>
    <p>When we receive a valid DMCA takedown notice, we will:</p>
    <ul>
        <li><strong>Acknowledge receipt</strong> within 2 business days</li>
        <li><strong>Review the notice</strong> for completeness and validity</li>
        <li><strong>Remove or disable access</strong> to the allegedly infringing content promptly if the notice is
            valid</li>
        <li><strong>Notify the user</strong> who posted the content about the takedown and provide information about
            counter-notice procedures</li>
    </ul>

    <h2 id="infringement-actions">4. Infringement Actions</h2>
    <p><strong>Content Removal:</strong></p>
    <p>Upon receiving a valid DMCA notice, we will expeditiously remove or disable access to the material that is
        claimed to be infringing. This action is taken without prejudice to the rights of either party.</p>

    <p><strong>User Notification:</strong></p>
    <p>We will make reasonable efforts to notify the user who posted the content about the removal and provide them
        with:</p>
    <ul>
        <li>A copy of the DMCA notice (with personal information redacted as appropriate)</li>
        <li>Information about their right to file a counter-notice</li>
        <li>Instructions on the counter-notice process</li>
    </ul>

    <h2 id="filing-claim">5. Filing a Copyright Claim</h2>

    <h3 id="claim-requirements">5.1 Claim Requirements</h3>
    <p>To file a valid DMCA takedown notice, you must be:</p>
    <ul>
        <li>The copyright owner of the allegedly infringed work, or</li>
        <li>An agent authorized to act on behalf of the copyright owner</li>
    </ul>

    <p><strong>Good Faith Requirement:</strong> You must have a good faith belief that the use of the material is not
        authorized by the copyright owner, its agent, or the law.</p>

    <h3 id="required-information">5.2 Required Information</h3>
    <p>Your DMCA notice must include all of the following information:</p>
    <ol>
        <li><strong>Physical or electronic signature</strong> of the copyright owner or authorized agent</li>
        <li><strong>Identification of the copyrighted work</strong> claimed to have been infringed (provide title,
            author, registration number if applicable)</li>
        <li><strong>Identification of the infringing material</strong> and its location on The Forge (provide specific
            URLs or mod names)</li>
        <li><strong>Your contact information</strong> including name, address, telephone number, and email address</li>
        <li><strong>A statement of good faith belief</strong> that use of the material is not authorized</li>
        <li><strong>A statement of accuracy</strong> that the information in the notice is accurate</li>
        <li><strong>A statement under penalty of perjury</strong> that you are authorized to act on behalf of the
            copyright owner</li>
        <li><strong>Specific description</strong> of how the material infringes your copyright</li>
    </ol>

    <p><strong>Incomplete Notices:</strong> Notices that do not include all required information may be rejected or may
        delay processing.</p>

    <h2 id="counter-claims">6. Counter-Claims</h2>
    <p>If you believe your content was removed in error or misidentification, you may file a counter-notice. To be
        valid, your counter-notice must include:</p>

    <ol>
        <li><strong>Your physical or electronic signature</strong></li>
        <li><strong>Identification of the material</strong> that was removed and its previous location</li>
        <li><strong>A statement under penalty of perjury</strong> that you have a good faith belief the material was
            removed by mistake or misidentification</li>
        <li><strong>Your contact information</strong> including name, address, telephone number, and email address</li>
        <li><strong>A statement consenting</strong> to the jurisdiction of the federal district court for your address
            (or federal district court where The Forge is located if you are outside the US)</li>
        <li><strong>A statement agreeing</strong> to accept service of process from the person who provided the original
            DMCA notice</li>
    </ol>

    <p><strong>Counter-Notice Process:</strong></p>
    <ul>
        <li>We will forward your counter-notice to the original complainant</li>
        <li>If no lawsuit is filed within 10 business days, we may restore the content</li>
        <li>We reserve the right to refuse restoration in cases involving repeat infringers</li>
    </ul>

